/*

Operadores logicos

Operador lógico AND (&&): Este operador lógico compara dos expresiones. Se debe de tener ambas expresiones 
verdaderas para que el resultado sea verdadero, pero si una o ambas expresiones son falsas, la sentencia completa se devolverá como falsa.

Operador lógico OR (||): Este devuelve como true si cualquiera de las dos expresiones son verdaderas y false cuando
ambas expresiones son falsas. Ambas expresiones pueden ser verdaderas, pero solo se necesita una para que el resultado sea verdadero.

Operador lógico NOT (!): El operador lógico NOT no realiza ninguna comparación como lo hacen los operadores AND y OR.

Este se utiliza con un símbolo "!" (signo de exclamación) para representar un operador NOT o negación.

*/


const a = 10
const b = 20
const c = "10"

console.log( a == b && a === c)
console.log( a != b || a === c)
console.log(!(a === c))














 

